package Custom;

public class PathPair {
	private String path1;
	private String path2;
	
	public PathPair(String path1,String path2) {
		this.path1=path1;
		this.path2=path2;
	}
	/**
	 * returns first path in the pair*/
	public String getPath1() {
		return path1;
	}
	public String getPath2() {
		return path2;
	}
	
}
